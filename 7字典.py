# 使用字典记录员工信息
employees = [
    {"姓名": "王力鸿", "部门": "科技部", "工资": 3000, "级别": 1},
    {"姓名": "周杰轮", "部门": "市场部", "工资": 5000, "级别": 2},
    {"姓名": "林俊节", "部门": "市场部", "工资": 7000, "级别": 3},
    {"姓名": "张学油", "部门": "科技部", "工资": 4000, "级别": 1},
    {"姓名": "刘德清", "部门": "市场部", "工资": 6000, "级别": 2}
]

print("员工原始信息:")
print("=" * 40)
print(f"{'姓名':<6} {'部门':<6} {'工资':<6} {'级别':<6}")
print("-" * 40)
for emp in employees:
    print(f"{emp['姓名']:<6} {emp['部门']:<6} {emp['工资']:<6} {emp['级别']:<6}")
print("=" * 40)